-- MAX/MIN
SELECT MAX(SAL),MIN(SAL)
FROM EMP;

-- AVG
SELECT AVG(SAL)
FROM EMP;

-- GROUP BY
SELECT DEPTNO, COUNT(*)
FROM EMP
GROUP BY DEPTNO;

SELECT DEPTNO, ENAME, COUNT(*) -- 오류 남
FROM EMP
GROUP BY DEPTNO;

--HAVING
SELECT DEPTNO, JOB, AVG(SAL)
FROM EMP
GROUP BY DEPTNO, JOB
HAVING AVG(SAL)>=2000; -- 집계 함수를 대상으로 조건을 줌

-- HAVING 대신 WHERER 사용
SELECT DEPTNO, JOB, AVG(SAL)
FROM EMP
WHERE AVG(SAL)>=2000  -- 오류 남
GROUP BY DEPTNO, JOB;

-- HAVING, WHERE
SELECT DEPTNO, JOB, AVG(SAL)
FROM EMP
WHERE SAL<=3000
GROUP BY DEPTNO, JOB
HAVING AVG(SAL)>=2000
ORDER BY 1,2 ;





